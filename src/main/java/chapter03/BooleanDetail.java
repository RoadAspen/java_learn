package chapter03;

/**
 * 布尔类型详情
 */
public class BooleanDetail {
    public static void main(String[] args) {
        /**
         * 布尔类型只有两种， true 和 false
         */
        boolean isPass = false;

        if(isPass){
            System.out.println("考试通过");
        }else{
            System.out.println("考试没有通过,下次努力");
        }
    }
}
